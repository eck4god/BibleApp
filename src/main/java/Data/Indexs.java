package main.java.Data;

public class Indexs {
    private Long indexId;
    private Long bibleId;
    private Long bookId;
    private Long chapterId;
    private Long verseId;
    private Long bibleLinkId;

    public Long getIndexId() {
        return indexId;
    }

    public void setIndexId(Long indexId) {
        this.indexId = indexId;
    }

    public Long getBibleId() {
        return bibleId;
    }

    public void setBibleId(Long bibleId) {
        this.bibleId = bibleId;
    }

    public Long getBookId() {
        return bookId;
    }

    public void setBookId(Long bookId) {
        this.bookId = bookId;
    }

    public Long getChapterId() {
        return chapterId;
    }

    public void setChapterId(Long chapterId) {
        this.chapterId = chapterId;
    }

    public Long getVerseId() {
        return verseId;
    }

    public void setVerseId(Long verseId) {
        this.verseId = verseId;
    }

    public Long getBibleLinkId() {
        return bibleLinkId;
    }

    public void setBibleLinkId(Long bibleLinkId) {
        this.bibleLinkId = bibleLinkId;
    }
}
